# Day 5 - Python Operators

# NoneType
x = None
print(x)

# Assignment operators
a = 10
a += 5
print(a)

# Comparison operators
print(a > 10)
print(a == 15)

# Logical operators
print(a > 5 and a < 20)
print(a > 20 or a == 15)

# Membership operators
text = "Python Programming"
print("Python" in text)
print("Java" not in text)

# Bitwise operators
p = 5   # 0101
q = 3   # 0011
print(p & q)  # AND
print(p | q)  # OR
print(p ^ q)  # XOR
